function slugify(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function commonsOnly(url) {
  if (typeof url !== 'string' || !url.includes('/commons/')) return null;
  return url;
}

async function fetchSummary(title) {
  const url = `https://en.wikipedia.org/api/rest_v1/page/summary/${encodeURIComponent(title)}`;
  const response = await fetch(url, {
    headers: {
      'user-agent': 'Sippomi local data builder/1.0',
      accept: 'application/json',
    },
  });

  if (!response.ok) {
    throw new Error(`${title}: HTTP ${response.status}`);
  }

  const data = await response.json();
  return {
    description: data.description || '',
    extract: data.extract || '',
    pageUrl: data?.content_urls?.desktop?.page || '',
    imageUrl: commonsOnly(data?.originalimage?.source || data?.thumbnail?.source || ''),
  };
}

function createImageAlt(seed) {
  return `${seed.ownerName}に関係する${seed.name}の写真`;
}

function createOwnerImageAlt(seed) {
  return `${seed.ownerName}の写真`;
}

async function buildRecord(seed) {
  const petSummary = await fetchSummary(seed.petTitle);
  if (!petSummary.imageUrl) {
    throw new Error(`No reusable pet image for ${seed.petTitle}`);
  }

  let ownerSummary = null;
  if (seed.ownerTitle) {
    try {
      ownerSummary = await fetchSummary(seed.ownerTitle);
    } catch {
      ownerSummary = null;
    }
  }

  return {
    id: slugify(`${seed.ownerName}-${seed.name}`),
    entityType: seed.entityType,
    ownerName: seed.ownerName,
    ownerCategory: seed.ownerCategory,
    ownerWork: seed.ownerWork,
    ownerCountry: seed.ownerCountry,
    ownerImageUrl: ownerSummary?.imageUrl || null,
    ownerImageAlt: ownerSummary?.imageUrl ? createOwnerImageAlt(seed) : null,
    ownerSourceUrl: ownerSummary?.pageUrl || '',
    name: seed.name,
    petName: seed.name,
    reading: seed.reading,
    petReading: seed.reading,
    species: [seed.species],
    petSpecies: seed.species,
    breed: seed.breed,
    summary: seed.summary,
    story: seed.story,
    meaning: seed.summary,
    namingHint: seed.namingHint,
    vibe: seed.vibe,
    tags: seed.tags,
    imageUrl: petSummary.imageUrl,
    imageAlt: createImageAlt(seed),
    imageCredit: 'Photo via Wikimedia Commons / Wikipedia',
    sourceUrl: petSummary.pageUrl,
    sourceLabel: seed.petTitle,
    sourceCheckedAt: SOURCE_CHECKED_AT,
    ownerDescription: ownerSummary?.description || '',
    petDescription: petSummary.description || '',
  };
}

const records = [];
for (const seed of SEED_ENTRIES) {
  records.push(await buildRecord(seed));
}

await writeFile(OUTPUT_PATH, `${JSON.stringify(records, null, 2)}\n`, 'utf8');
console.log(`Wrote ${records.length} records to ${OUTPUT_PATH.pathname}`);
