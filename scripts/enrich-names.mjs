function toHiragana(s) {
  // Convert katakana to hiragana
  return s.replace(/[\u30A1-\u30F6]/g, ch => String.fromCharCode(ch.charCodeAt(0) - 0x60));
}

function makeEntry(name, reading, baseMeaning, gender, vibe, color, species, themes, seed) {
  const v = [...new Set([...(vibe || []), THEME_VIBE_MAP[themes[0]] || ''])].filter(Boolean);
  const c = color || 'なし';
  const wish = deriveWish(name, gender, v, themes);
  const scene = deriveScene(name, gender, v, themes);
  const uq = deriveUniqueness(name);
  const len = deriveLength(name);
  const genderText = gender === 'オス' ? '元気で活発' : gender === 'メス' ? '優しく愛らしい' : 'バランスの取れた';
  const meaning = `由来: ${baseMeaning}。響き: 「${name}」と呼ぶときの語感が心地よい。性格: ${genderText}な${v.join('・')}の雰囲気を持つ名前。`;
  return { name, reading, meaning, species: [...species], gender, vibe: v, color: [c], length: len, theme: themes, wish: [wish], uniqueness: uq, scene: [scene] };
}

