function pick(arr, seed) {
  return arr[((typeof seed === 'number' ? seed : seed.charCodeAt(0)) + Math.floor(Math.random() * 100)) % arr.length];
}

function hash(s) { let h = 0; for (let i = 0; i < s.length; i++) { h = ((h << 5) - h) + s.charCodeAt(i); h |= 0; } return Math.abs(h); }

function deriveWish(name, gender, vibe, theme) {
  const v = (vibe || []).join(' '); const t = (theme || []).join(' ');
  if (/かっこいい|武骨|クール/.test(v)) return '強さ';
  if (/癒し系|ふわふわ|かわいい/.test(v)) return 'やさしさ';
  if (/知的|洗練/.test(v)) return '知性';
  if (/神秘/.test(v)) return '幸運';
  if (/陽気|元気|個性的/.test(v)) return '健やかさ';
  if (/天体|宇宙|神話/.test(t)) return '幸運';
  if (/和風|古風/.test(t)) return 'やさしさ';
  if (/宝石|貴金属/.test(t)) return '強さ';
  if (gender === 'オス') return '強さ';
  if (gender === 'メス') return 'やさしさ';
  return '健やかさ';
}

function deriveScene(name, gender, vibe, theme) {
  const v = (vibe || []).join(' '); const t = (theme || []).join(' ');
  if (/神秘的|ミステリアス/.test(v)) return '静かな夜みたい';
  if (/陽気|元気/.test(v)) return '朝の光みたい';
  if (/癒し系|ふわふわ/.test(v)) return 'ぬくもり重視';
  if (/洗練|上品|知的/.test(v)) return '凛とした空気';
  if (/和風/.test(v) || /和風|古風/.test(t)) return '季節を感じる';
  if (/自然|植物/.test(t) || /季節/.test(t)) return '季節を感じる';
  if (/神話|天体|宇宙/.test(t)) return '静かな夜みたい';
  if (/地理|地名|洋風|モダン/.test(t)) return '凛とした空気';
  if (/食べ物|スイーツ/.test(t)) return 'ぬくもり重視';
  return '静かな夜みたい';
}

function deriveUniqueness(name) {
  const len = [...name.replace(/ー/g, '')].length;
  return len <= 2 ? '定番' : len >= 4 ? 'ユニーク' : 'ほどよい';
}

function deriveLength(name) {
  const clen = [...name.replace(/ー/g, '')].length;
  return clen <= 2 ? '2' : clen <= 3 ? '3' : '4+';
}

function extendMeaning(entry) {
  const existing = entry.meaning || '';
  if (/^由来[：:]/.test(existing.trim())) return existing;
  const vibeText = (entry.vibe && entry.vibe.length > 0) ? entry.vibe.join('・') + 'の雰囲気' : '和やかな雰囲気';
  const genderText = entry.gender === 'オス' ? '元気で活発' : entry.gender === 'メス' ? '優しく愛らしい' : 'バランスの取れた';
  return `由来: ${existing}。響き: 「${entry.name}」と呼ぶときの語感が心地よい。性格: ${genderText}な${vibeText}を持つ名前。`;
}

